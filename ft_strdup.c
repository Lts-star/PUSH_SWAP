#include "push_swap.h"

char	*ft_strdup(const char *src)
{
	char	*str;
	int		i;
	int		size;

	size = 0;
	while (src[size])
		++size;
	str = malloc(sizeof(char) * size + 1);
	if (!(str))
		return (NULL);
	i = 0;
	while (src[i])
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
